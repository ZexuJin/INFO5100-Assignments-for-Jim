/*
*
* The difference between final and finally.
*
* Final:
* Making a class final means that there cannot be extend classes.
* Adding final to a method means that the method cannot be overrode.
* Putting final in front of a variable or a parameter means that once the reference has been assigned then it cannot be changed.
*
* Finally:
* It's a keyword in exception handling.
* The finally block is an optional block to use with a try/catch statement.
* There are times when we have code that needs to execute regardless of whether an exception occurs,
* and this is where the finally keyword comes in.
 * */